public class LoginGoogleServlet extends HttpServlet {

    private static final long serialVersionUID = 1L;

    public LoginGoogleServlet() {
        super();
    }

    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String code = request.getParameter("code");
        if (code == null || code.isEmpty()) {
            RequestDispatcher dis = request.getRequestDispatcher("login.jsp");
            dis.forward(request, response);
        } else {
            String accessToken = GoogleUtils.getToken(code);
            User googlePojo = GoogleUtils.getUserInfo(accessToken);
            request.setAttribute("username", googlePojo.getUsername());
            request.setAttribute("password", googlePojo.getPassword());
            request.setAttribute("role", googlePojo.getRole());
            RequestDispatcher dis = request.getRequestDispatcher("normalSuccess.jsp");
            dis.forward(request, response);
        }
    }

    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        doGet(request, response);
    }
}
